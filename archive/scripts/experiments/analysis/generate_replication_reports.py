def _family_issue_files(out_dir: Path, family: str) -> list[str]:
    files: list[str] = []
    for path in sorted((out_dir / "issues").glob("*.md")):
        if path.name.lower() == "readme.md":
            continue
        text = path.read_text(encoding="utf-8")
        marker = f"- family: `{family}`"
        if marker in text:
            files.append(path.name)
    return files


def _suite_scaling_link(out_dir: Path, family: str) -> str:
    path = out_dir / "comparisons" / family / "suite_scaling.md"
    return f"`comparisons/{family}/suite_scaling.md`" if path.exists() else "`-`"


