def _write_commands_md(out_dir: Path, entries: list[dict[str, Any]]) -> Path:
    path = out_dir / "commands.md"
    groups: dict[str, list[dict[str, Any]]] = defaultdict(list)
    for entry in entries:
        groups[entry["category"]].append(entry)

    lines = ["# Commands", ""]
    for category in ("examples", "speed", "suites", "reports"):
        bucket = sorted(groups.get(category, []), key=lambda entry: (entry["family"], entry["id"]))
        if not bucket:
            continue
        lines.extend([f"## {category.title()}", ""])
        for entry in bucket:
            lines.append(f"### {entry['id']}")
            lines.append("")
            lines.append(f"- family: `{entry['family']}`")
            lines.append(f"- source: `{entry['source']}`")
            lines.append(f"- outputs: `{', '.join(entry['outputs'])}`")
            lines.append("")
            lines.append("```bash")
            lines.append(entry["command"])
            lines.append("```")
            lines.append("")
    path.write_text("\n".join(lines), encoding="utf-8")
    return path


