def _rel(path: Path) -> str:
    try:
        return str(path.resolve().relative_to(REPO_ROOT.resolve()))
    except ValueError:
        return str(path.resolve())


def _load_manifest(out_dir: Path) -> list[dict[str, Any]]:
    payload = json.loads((out_dir / "manifest.json").read_text(encoding="utf-8"))
    return list(payload.get("entries", []))


def _entries_by_id(entries: list[dict[str, Any]]) -> dict[str, dict[str, Any]]:
    return {entry["id"]: entry for entry in entries}


def _markdown_table(headers: list[str], rows: list[list[object]]) -> str:
    lines = [
        "| " + " | ".join(headers) + " |",
        "| " + " | ".join("---" for _ in headers) + " |",
    ]
    for row in rows:
        lines.append("| " + " | ".join(str(cell) for cell in row) + " |")
    return "\n".join(lines)


def _write_csv(path: Path, rows: list[dict[str, Any]], fieldnames: list[str]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", newline="", encoding="utf-8") as handle:
        writer = csv.DictWriter(handle, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(rows)


def _suite_rows_from_manifest(entry: dict[str, Any]) -> list[dict[str, Any]]:
    summary_path = REPO_ROOT / entry["outputs"][0]
    payload = json.loads(summary_path.read_text(encoding="utf-8"))
    return list(payload.get("rows", []))


def _build_direct_speed_rows(entries: list[dict[str, Any]], family: str) -> list[dict[str, Any]]:
    grouped: dict[tuple[str, str], list[dict[str, Any]]] = defaultdict(list)
    for entry in entries:
        if entry["family"] != family or entry["kind"] != "speed_repeat":
            continue
        summary = dict(entry.get("summary", {}))
        case_id = str(summary.get("case_id", summary.get("nx", "")))
        if "case_id" not in summary:
            if "nx" in summary and "ny" in summary:
                case_id = f"nx{summary['nx']}_ny{summary['ny']}_np{summary['nprocs']}"
            elif "level" in summary and "nprocs" in summary and "total_steps" in summary:
                case_id = f"l{summary['level']}_steps{summary['total_steps']}_np{summary['nprocs']}"
            elif "level" in summary and "nprocs" in summary:
                case_id = f"l{summary['level']}_np{summary['nprocs']}"
        grouped[(case_id, summary["implementation"])].append(summary)

    rows: list[dict[str, Any]] = []
    for (case_id, implementation), samples in sorted(grouped.items()):
        status_set = sorted({str(sample.get("result", "unknown")) for sample in samples})
        row: dict[str, Any] = {
            "case_id": case_id,
            "implementation": implementation,
            "repeats": len(samples),
            "status": ",".join(status_set),
            "mpi_ranks": int(samples[0].get("nprocs", 1)),
            "median_wall_time_s": float(median(float(sample.get("wall_time_s", 0.0)) for sample in samples)),
            "median_newton_iters": int(median(int(sample.get("total_newton_iters", 0)) for sample in samples)),
            "median_linear_iters": int(median(int(sample.get("total_linear_iters", 0)) for sample in samples)),
        }
        if any(sample.get("final_energy") is not None for sample in samples):
            row["median_final_energy"] = float(
                median(float(sample.get("final_energy", 0.0)) for sample in samples if sample.get("final_energy") is not None)
            )
        if any(sample.get("final_compliance") is not None for sample in samples):
            row["median_final_compliance"] = float(
                median(float(sample.get("final_compliance", 0.0)) for sample in samples if sample.get("final_compliance") is not None)
            )
        if any(sample.get("final_volume_fraction") is not None for sample in samples):
            row["median_final_volume_fraction"] = float(
                median(
                    float(sample.get("final_volume_fraction", 0.0))
                    for sample in samples
                    if sample.get("final_volume_fraction") is not None
                )
            )
        rows.append(row)
    return rows


def _write_direct_speed_reports(out_dir: Path, entries: list[dict[str, Any]]) -> list[Path]:
    created: list[Path] = []
    for family, cfg in FAMILY_CONFIG.items():
        rows = _build_direct_speed_rows(entries, family)
        if not rows:
            continue
        family_dir = out_dir / "comparisons" / family
        csv_path = family_dir / "direct_speed.csv"
        md_path = family_dir / "direct_speed.md"

        fieldnames = sorted({key for row in rows for key in row.keys()})
        _write_csv(csv_path, rows, fieldnames)
        table_rows = []
        for row in rows:
            table_rows.append(
                [
                    row["case_id"],
                    row["implementation"],
                    row["status"],
                    row["median_wall_time_s"],
                    row["median_newton_iters"],
                    row["median_linear_iters"],
                    row.get("median_final_energy", ""),
                    row.get("median_final_compliance", ""),
                    row.get("median_final_volume_fraction", ""),
                ]
            )
        md_path.write_text(
            "\n".join(
                [
                    f"# {cfg['title']} Direct Speed Comparison",
                    "",
                    "Median wall time and iteration counts across the three dedicated direct-CLI repetitions.",
                    "",
                    _markdown_table(
                        [
                            "case",
                            "implementation",
                            "status",
                            "median wall [s]",
                            "median Newton",
                            "median linear",
                            "median energy",
                            "median compliance",
                            "median volume",
                        ],
                        table_rows,
                    ),
                    "",
                ]
            ),
            encoding="utf-8",
        )
        created.extend([csv_path, md_path])
    return created


